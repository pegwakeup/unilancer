export type TranslationSourceKey = keyof typeof staticTranslationTexts;

export function getAllStaticTranslations() {
  const allTranslations: Array<{ context: string; text: string }> = [];

  for (const [context, texts] of Object.entries(staticTranslationTexts)) {
    for (const text of texts) {
      allTranslations.push({ context, text });
    }
  }

  return allTranslations;
}
